print("range(10)")
for i in range(10):
    print(i)


print("\nrange(1, 10)")
for i in range(1, 10):
    print(i)


print("\nrange(0, 10, 3)")
for i in range(0, 10, 3): # de 0 a 10 - 1, de 3 em 3
    print(i)


print("\nrange(10, 0, -1)") 
for i in range(10, 0, -1):
    print(i)


print("\nrange(10, 0, -2)") 
for i in range(10, 0, -2):
    print(i)

lista = list(range(10))
print(f"\n{lista}")
